namespace Pancake.Apex
{
    public enum ColorTarget
    {
        /// <summary>
        /// Global tinting color for the element.
        /// </summary>
        Global,

        /// <summary>
        /// Tinting color for all text rendered by the element.
        /// </summary>
        Content,

        /// <summary>
        /// Tinting color for all background rendered by the element.
        /// </summary>
        Background
    }
}